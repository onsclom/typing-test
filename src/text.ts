const fontKey = "0123456789ABCDEFGHIJKLMNOPQRSTUVWXYZ";

type CharLines = [number, number, number, number][];

export function getCharLines(char: string): CharLines {
  const charIndex = fontKey.indexOf(char.toUpperCase());
  if (charIndex === -1) return [];
  return chars[charIndex] as CharLines;
}

export function drawChar(
  ctx: CanvasRenderingContext2D,
  lines: CharLines,
  tx: number,
  ty: number,
  width: number,
  gridWidth: number // should be 5
) {
  function calcOffset(n: number) {
    return n * (width / gridWidth) + width / gridWidth / 2;
  }

  for (const line of lines) {
    ctx.beginPath();
    ctx.moveTo(calcOffset(line[0]) + tx, ty + calcOffset(line[1]));
    ctx.lineTo(calcOffset(line[2]) + tx, ty + calcOffset(line[3]));
    ctx.stroke();
  }
}
